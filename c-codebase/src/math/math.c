#include "math/math.h"

#include <math.h>

f32
math_sqrt(f32 x)
{
    return sqrtf(x);
}

f32
math_tan(f32 x)
{
    return tanf(x);
}

f32
math_cos(f32 x)
{
    return cosf(x);
}

f32
math_sin(f32 x)
{
    return sinf(x);
}

f32
math_acos(f32 x)
{
    return acosf(x);
}

f32
math_abs(f32 x)
{
    return fabsf(x);
}
